fn main() {
    let arr = vec![0];
    println!("vec: {:?}", arr);
    println!("middle: {}", arr.len() / 2);
    let arr = vec![0, 1];
    println!("vec: {:?}", arr);
    println!("middle: {}", arr.len() / 2);
    let arr = vec![0, 1, 2];
    println!("vec: {:?}", arr);
    println!("middle: {}", arr.len() / 2);
    let arr = vec![0, 1, 2, 3];
    println!("vec: {:?}", arr);
    println!("middle: {}", arr.len() / 2);
    let arr = vec![0, 1, 2, 3, 4];
    println!("vec: {:?}", arr);
    println!("middle: {}", arr.len() / 2);
}
